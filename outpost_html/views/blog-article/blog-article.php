            <div class="content_width">
                <p class="first_paragraph">It's no secret that New York City is one of the most expensive
                    places you can live — it ranks as <span class="ba_row">NUMBER ONE</span> among U.S. cities
                    year after year, and is consistently within the top 10 most expen-
                    sive places to live <span class="ba_row">WORLDWIDE</span>. And while that might deter some
                    eager wanderers from making the Big Apple home, there are
                    plenty of ways you can save money and still experience all New
                    York has to offer.</p>

                <div class="oc_paragraphs">
                    <div class="blog_paragraph">
                        <h3 class="first_title">Transportation</h3>
                        <p>We're not above a super-late night Uber ride, but let's be real,
                            taxis and shared ride services add up—and quick. Getting
                            aquainted with the MTA systems is one of the best ways you can
                            save your hard-earned money in the city, as you can get virtually
                            anywhere for the cost of 2.75. We recommend tallying up your
                            monthly MTA charges before deciding if the month pass is worth
                            the cost—it'll be $127 for a month of unlimited swipes or $33 for a
                            week. If you're working from home or only taking the train on the
                            weekends you can probably save your coins and purchase indi-
                            vidual tickets.</p>
                        <br>
                        <p>And in warmer months, biking is another affordable alternative
                            so you can skip the wait times or crowded L train. Buying a bike
                            of your own will be more of a long-term investment, or you can
                            opt for a membership with <span class="ba_row">CITI BIKE</span>, which are stationed all over
                            the five boroughs. A one-month membership is $15, or you can
                            get set-up for a full year for just $179.</p>
                    </div>

                    <div class="blog_paragraph">
                        <h3 class="first_title">Housing</h3>
                        <p>Youll likely end up spending the largest amount on money on
                            housing, as New York is also designated as the most <span class="ba_row">EXPENSIVE
                                PLACE TO RENT</span> in the US. Don't let that discourage you—be-
                            cause there are more affordable options out there. The most es-
                            sential tip is to plan on having roommates, as you'll split the
                            apartment cost into a fraction of its original price. We also rec-
                            ommend looking outside of Manhattan for your next home, as it
                            is the most expensive borough for both renting and day-to-day
                            costs. And if you want to save money without skipping out on
                            amenities like furnished apartments, laundry or even in-building
                            fitness studios, co-living spaces are a no-stress way to get set-
                            tled in the city on a budget.</p>
                    </div>

                    <div class="blog_paragraph">
                        <h3 class="first_title">Food</h3>
                        <p>You won't want to miss out on the global cuisine NYC has to offer,
                            with everything from vegan Ethiopian eats to flavorful hot pot to
                            try. For meals out on a budget, scour your local listings for happy
                            hours, bottomless brunch or other deals so you can save while
                            still enjoying the city's foodie scene. We also recommend stray-
                            ing from the crowds and Insta-famous restaurants if you're seri-
                            ‘ous about saving money—there are plenty of authentic restau-
                            rants that won't cost an arm and a leg for an appetizer.</p>
                        <br>
                        <p>And when it comes time to grocery shop, compare prices from
                            your nearest stores to find the cheapest option. If the nearest
                            stores are all priced at a similar level (and a bit out-of-budget),
                            you may want to trek to the nearest Trader Joe's. While not as
                            convenient as walking down the block, you can usually save a
                            significant amount of money on your weekly haul. For produce,
                            always consult your local bodega to get unreal deals on fruits
                            and vegetables. A grocery store might charge you as much as $5
                            for a container of blackberries, which you can get for around $1
                            at your nearest market.</p>
                    </div>

                    <div class="blog_paragraph">
                        <h3 class="first_title">Entertaiment</h3>
                        <p>One of the greatest parts of NYC is the never-ending myriad of
                            places to see—from namesake museums to the next show on
                            Broadway. You can still experience all the city has to offer with-
                            out disrupting your budget with a bit of prior planning. Almost
                            every museum in the city <span class="ba_row">OFFERS A FREE OR PAY-WHAT-YOU-
                                CAN DAY</span>, so you can get access to places like the MoMa or Gug-
                            genheim Museum without dropping a dime.</p>
                        <br>
                        <p>You can also get your very own <span class="ba_row">NYC ID</span>, which unlocks deals, dis-
                            counts and free tickets to a plethora of events, shows and enter-
                            tainment throughout the city. It's free, and all you'll need to qual-
                            ify is a proof of residence. Browse sites live <span class="ba_row">EVENTBRITE</span> or the
                            <span class="ba_row">NEW YORK PUBLIC LIBRARY</span> page to find free things to do any
                            night of the week, whether you're in a mood for a comedy show
                            or dance party.</p>
                    </div>
                </div>
